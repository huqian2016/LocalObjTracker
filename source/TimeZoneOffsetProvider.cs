        /// <summary>
        /// Gets the UTC offset as a TimeSpan for a given time zone string.
        /// </summary>
        /// <param name="timeZoneString">The time zone string (e.g., "Pacific/Kiritimati").</param>
        /// <returns>The UTC offset as a TimeSpan.</returns>
        public static TimeSpan GetUtcOffset(string timeZoneString)
        {
            if (string.IsNullOrWhiteSpace(timeZoneString))
            {
                throw new ArgumentException("Time zone string cannot be null or empty.", nameof(timeZoneString));
            }

            if (TimeZoneOffsetMap.TryGetValue(timeZoneString, out TimeSpan offset))
            {
                return offset;
            }
            else
            {
                throw new KeyNotFoundException($"The time zone '{timeZoneString}' is not in the offset map.");
            }
        }

        public static DateTime ConvertToUtc(DateTime localTime, string timeZoneString)
        {
            TimeSpan offset = GetUtcOffset(timeZoneString);
            return localTime - offset;
        }
